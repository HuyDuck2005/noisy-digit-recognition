import React, { useMemo, useState } from 'react';
import { resolveApiUrl } from '../../services/api';

const SORTERS = {
  index: (a, b) => a.index - b.index,
  confidence: (a, b) => b.confidence - a.confidence,
  area: (a, b) => b.area - a.area,
  x: (a, b) => a.x - b.x,
  y: (a, b) => a.y - b.y,
  label: (a, b) => String(a.label || '').localeCompare(String(b.label || '')),
};

const statusStyle = (status) => {
  if (status === 'normal') return { color: '#86efac', bg: 'rgba(34,197,94,0.12)', border: 'rgba(34,197,94,0.3)' };
  return { color: '#fca5a5', bg: 'rgba(239,68,68,0.12)', border: 'rgba(239,68,68,0.3)' };
};

const BoundingBoxTable = ({ boxes = [] }) => {
  const [sortBy, setSortBy] = useState('index');
  const [customLabels, setCustomLabels] = useState({});

  const sorted = useMemo(() => [...boxes].sort(SORTERS[sortBy] || SORTERS.index), [boxes, sortBy]);

  const exportCSV = () => {
    const rows = [
      ['index', 'x', 'y', 'width', 'height', 'area', 'aspect_ratio', 'label', 'confidence', 'status', 'top_k'],
      ...boxes.map((box) => [
        box.index,
        box.x,
        box.y,
        box.width,
        box.height,
        box.area,
        box.aspect_ratio,
        customLabels[box.index] || box.label,
        box.confidence,
        box.status,
        (box.top_k || []).map((item) => `${item.label}:${item.confidence}`).join('|'),
      ]),
    ];
    const csv = rows.map((row) => row.map((cell) => `"${String(cell ?? '').replaceAll('"', '""')}"`).join(',')).join('\n');
    const url = URL.createObjectURL(new Blob([csv], { type: 'text/csv;charset=utf-8' }));
    const link = document.createElement('a');
    link.href = url;
    link.download = 'bounding_boxes.csv';
    link.click();
    URL.revokeObjectURL(url);
  };

  return (
    <div className="rounded-2xl overflow-hidden" style={{ background: 'rgba(13,30,60,0.7)', border: '1px solid rgba(56,189,248,0.15)' }}>
      <div className="px-5 py-4 flex flex-wrap gap-3 items-center justify-between" style={{ borderBottom: '1px solid rgba(56,189,248,0.1)' }}>
        <div>
          <h3 className="font-bold text-slate-200 text-sm">Bounding Boxes</h3>
          <p className="text-[11px] mt-0.5" style={{ color: '#64748b' }}>Labels are generated by mock recognizer in current phase.</p>
        </div>
        <div className="flex items-center gap-2">
          <select className="form-select" value={sortBy} onChange={(e) => setSortBy(e.target.value)} style={{ width: 150, padding: '8px 10px', fontSize: 12 }}>
            {Object.keys(SORTERS).map((key) => <option key={key} value={key}>Sort: {key}</option>)}
          </select>
          <button onClick={exportCSV} disabled={!boxes.length} className="btn btn-sm btn-secondary disabled:opacity-40">
            CSV
          </button>
        </div>
      </div>

      <div className="overflow-x-auto">
        <table className="w-full text-sm">
          <thead>
            <tr style={{ background: 'rgba(7,21,38,0.8)' }}>
              {['#', 'Crop', 'BBox', 'Area', 'Ratio', 'Label', 'Confidence', 'Status', 'Top K'].map((head) => (
                <th key={head} className="px-4 py-3 text-left text-[11px] font-bold uppercase tracking-widest" style={{ color: '#64748b' }}>{head}</th>
              ))}
            </tr>
          </thead>
          <tbody>
            {sorted.map((box, rowIndex) => {
              const style = statusStyle(box.status);
              const label = customLabels[box.index] ?? box.label;
              return (
                <tr key={box.index} style={{ borderTop: '1px solid rgba(56,189,248,0.06)', background: rowIndex % 2 === 0 ? 'rgba(13,30,60,0.3)' : 'transparent' }}>
                  <td className="px-4 py-3 font-mono text-xs" style={{ color: '#38bdf8' }}>#{box.index}</td>
                  <td className="px-4 py-3">
                    {box.crop_url ? (
                      <img src={resolveApiUrl(box.crop_url)} alt={`crop ${box.index}`} className="w-12 h-12 object-contain rounded-lg" style={{ background: '#040d1a', border: '1px solid rgba(56,189,248,0.12)' }} />
                    ) : (
                      <span className="text-xs text-slate-500">None</span>
                    )}
                  </td>
                  <td className="px-4 py-3 font-mono text-xs" style={{ color: '#94a3b8' }}>
                    {box.x}, {box.y}, {box.width}, {box.height}
                  </td>
                  <td className="px-4 py-3 text-xs" style={{ color: '#94a3b8' }}>{box.area}</td>
                  <td className="px-4 py-3 text-xs" style={{ color: '#94a3b8' }}>{Number(box.aspect_ratio || 0).toFixed(3)}</td>
                  <td className="px-4 py-3">
                    <input
                      value={label}
                      maxLength={2}
                      onChange={(e) => setCustomLabels({ ...customLabels, [box.index]: e.target.value })}
                      className="w-12 text-center rounded-lg py-1 font-black outline-none"
                      style={{ background: 'rgba(56,189,248,0.08)', color: '#e2e8f0', border: '1px solid rgba(56,189,248,0.2)' }}
                    />
                  </td>
                  <td className="px-4 py-3">
                    <div className="flex items-center gap-2">
                      <div className="w-16 h-1.5 rounded-full overflow-hidden" style={{ background: 'rgba(255,255,255,0.08)' }}>
                        <div className="h-full rounded-full" style={{ width: `${Math.max(0, Math.min(1, box.confidence || 0)) * 100}%`, background: style.color }} />
                      </div>
                      <span className="text-xs font-bold" style={{ color: style.color }}>{((box.confidence || 0) * 100).toFixed(1)}%</span>
                    </div>
                  </td>
                  <td className="px-4 py-3">
                    <span className="text-[10px] font-bold px-2 py-1 rounded-full" style={{ color: style.color, background: style.bg, border: `1px solid ${style.border}` }}>
                      {box.status}
                    </span>
                  </td>
                  <td className="px-4 py-3 text-xs" style={{ color: '#64748b' }}>
                    {(box.top_k || []).map((item) => `${item.label} ${(item.confidence * 100).toFixed(0)}%`).join(', ') || 'None'}
                  </td>
                </tr>
              );
            })}
          </tbody>
        </table>
      </div>

      {!boxes.length && (
        <div className="py-12 text-center">
          <p className="font-bold text-slate-400">No boxes detected</p>
          <p className="text-xs mt-1" style={{ color: '#64748b' }}>Try another threshold mode or lower min_area.</p>
        </div>
      )}
    </div>
  );
};

export default BoundingBoxTable;
