import React, { useEffect, useMemo, useState } from 'react';

const TAB_CONFIG = [
  ['original_url', 'Original', 'Uploaded / resized source image'],
  ['grayscale_url', 'Grayscale', 'Single-channel image'],
  ['contrast_url', 'Contrast', 'Contrast-enhanced image'],
  ['illumination_url', 'Illumination', 'Illumination-corrected image'],
  ['denoised_url', 'Denoised', 'Noise-reduced image'],
  ['sharpened_url', 'Sharpened', 'Optional stroke sharpening'],
  ['edge_map_url', 'Edge Map', 'Edge or stroke enhancement map'],
  ['dog_url', 'DoG', 'Difference of Gaussian branch'],
  ['log_url', 'LoG', 'Laplacian of Gaussian branch'],
  ['gabor_response_url', 'Gabor', 'Gabor stroke response'],
  ['gabor_binary_url', 'Gabor Binary', 'Thresholded Gabor response'],
  ['binary_url', 'Binary', 'Main foreground mask'],
  ['binary_otsu_url', 'Otsu', 'Otsu threshold branch'],
  ['binary_adaptive_url', 'Adaptive', 'Adaptive threshold branch'],
  ['binary_sauvola_url', 'Sauvola', 'Sauvola threshold branch'],
  ['binary_niblack_url', 'Niblack', 'Niblack threshold branch'],
  ['morphology_url', 'Morphology', 'Morphology-cleaned mask'],
  ['line_mask_url', 'Line Mask', 'Detected horizontal/vertical lines'],
  ['no_lines_url', 'No Lines', 'Mask after line removal'],
  ['components_url', 'Components', 'Connected component visualization'],
  ['mser_regions_url', 'MSER', 'MSER proposal visualization'],
  ['fused_boxes_url', 'Fused Boxes', 'Merged/fused candidate boxes'],
  ['output_url', 'Output', 'Final candidate boxes'],
].map(([id, label, desc]) => ({ id, label, desc }));

const ImageTabs = ({ pipelineImages }) => {
  const tabs = useMemo(
    () => TAB_CONFIG.map((tab) => ({ ...tab, url: pipelineImages?.[tab.id] || '' })),
    [pipelineImages],
  );
  const firstAvailable = tabs.find((tab) => tab.url)?.id || TAB_CONFIG[0].id;
  const [active, setActive] = useState(firstAvailable);

  useEffect(() => {
    setActive(firstAvailable);
  }, [firstAvailable]);

  const activeTab = tabs.find((tab) => tab.id === active) || tabs[0];

  if (!pipelineImages) {
    return (
      <div className="card text-center" style={{ padding: 28 }}>
        <h3 className="card-title">Pipeline Images</h3>
        <p className="card-sub mt-2">Run the Advanced Classical CV pipeline to generate intermediate images.</p>
      </div>
    );
  }

  return (
    <div className="rounded-2xl overflow-hidden" style={{ background: 'rgba(13,30,60,0.7)', border: '1px solid rgba(56,189,248,0.15)' }}>
      <div className="px-5 py-4" style={{ borderBottom: '1px solid rgba(56,189,248,0.08)' }}>
        <h3 className="font-bold text-slate-200 text-sm">Advanced Pipeline Images</h3>
        <p className="text-[11px] mt-0.5" style={{ color: '#64748b' }}>Disabled tabs were not generated by current parameters.</p>
      </div>

      <div className="flex gap-1 p-2 overflow-x-auto" style={{ background: 'rgba(7,21,38,0.5)', borderBottom: '1px solid rgba(56,189,248,0.08)' }}>
        {tabs.map((tab, index) => (
          <button
            key={tab.id}
            onClick={() => tab.url && setActive(tab.id)}
            disabled={!tab.url}
            className="min-w-[96px] flex flex-col items-center py-2 px-2 rounded-xl text-center transition-all duration-200 disabled:opacity-35 disabled:cursor-not-allowed"
            style={{
              background: active === tab.id ? 'rgba(56,189,248,0.12)' : 'transparent',
              border: active === tab.id ? '1px solid rgba(56,189,248,0.35)' : '1px solid transparent',
            }}
          >
            <span className="text-[10px] font-bold font-mono" style={{ color: active === tab.id ? '#2dd4bf' : '#475569' }}>
              {String(index + 1).padStart(2, '0')}
            </span>
            <span className="text-[11px] font-semibold mt-0.5" style={{ color: active === tab.id ? '#e2e8f0' : '#64748b' }}>
              {tab.label}
            </span>
          </button>
        ))}
      </div>

      <div className="p-4">
        <div className="rounded-xl overflow-hidden flex items-center justify-center" style={{ background: '#040d1a', minHeight: 260, border: '1px solid rgba(56,189,248,0.08)' }}>
          {activeTab?.url ? (
            <img src={activeTab.url} alt={activeTab.label} className="w-full h-auto object-contain animate-fadeIn" style={{ maxHeight: 420 }} />
          ) : (
            <p className="text-sm text-slate-500">Stage not generated for current parameters.</p>
          )}
        </div>
        <p className="text-xs mt-3 px-3 py-2.5 rounded-xl" style={{ color: '#94a3b8', background: 'rgba(7,21,38,0.5)', border: '1px solid rgba(56,189,248,0.08)' }}>
          {activeTab?.desc}
        </p>
      </div>
    </div>
  );
};

export default ImageTabs;
